public class Even_NotEven {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.print("Число " + '='+' ' + i  + " Чётное");
                System.out.println();
            } else {
                System.out.print("Число " + '='+' ' + i  + " Не Чётное");
                System.out.println();
            }
        }
    }
}


